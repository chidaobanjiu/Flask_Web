class Tag(db.Model):
    __tablename__ = 'tags'
    id = db.Column(db.Integer, primary_key=True)
    name = db.Column(db.String(128), unique=True)
    default = db.Column(db.Boolean, default=False, index=True)

    def add_tag(self, t, post):
        tag = Tag.query.filter_by(name=t).first()
        if tag is None:
            tag = Tag(name=t)
        db.session.add(tag)
        db.session.commit()


    @staticmethod
    def insert_tags():
        tags = {
            'Python': False,
            'Flask': False,
            'BootStrap': False,
            'Blogs': True
        }
        for t in tags:
            tag = Tag.query.filter_by(name=t).first()
            if tag is None:
                tag = Tag(name=t)
            tag.default = tags[t]
            db.session.add(tag)
        db.session.commit()


    def __repr__(self):
        return '<Tag %r>' % self.name

    def to_json(self):
        json_tag = {
            'url': url_for('api.get_tag', id=self.id, _external=True),
            'tagname': self.name
        }
        return json_tag

    @staticmethod
    def from_json(json_tag):
        name = json_tag.get('name')
        if name is None or name == '':
            raise ValidationError('標籤不存在。')
        return Tag(name=name)



